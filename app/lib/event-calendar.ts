// ---------------------------------------------------------------
// 暦規則ジェネレータ
// ---------------------------------------------------------------
function iso(y: number, m: number, d: number): string {
  return `${y}-${String(m).padStart(2, "0")}-${String(d).padStart(2, "0")}`;
}

// 月の第 n 週の指定曜日（weekday: 0=日..6=土）の日付。存在しなければ null。
function nthWeekday(year: number, month: number, weekday: number, n: number): string | null {
  const first = new Date(Date.UTC(year, month - 1, 1));
  const firstDow = first.getUTCDay();
  const day = 1 + ((weekday - firstDow + 7) % 7) + (n - 1) * 7;
  const daysInMonth = new Date(Date.UTC(year, month, 0)).getUTCDate();
  if (day > daysInMonth) return null;
  return iso(year, month, day);
}

// 毎月第1金曜（米国雇用統計 NFP の通例）
function firstFridays(fromY: number, toY: number): string[] {
  const out: string[] = [];
  for (let y = fromY; y <= toY; y++) {
    for (let m = 1; m <= 12; m++) {
      const d = nthWeekday(y, m, 5, 1);
      if (d) out.push(d);
    }
  }
  return out;
}

// 毎月第2金曜（日本の株価指数オプション/先物 SQ）。quarterOnly=true でメジャーSQ（3/6/9/12月）。
function secondFridays(fromY: number, toY: number, quarterOnly: boolean): string[] {
  const out: string[] = [];
  for (let y = fromY; y <= toY; y++) {
    for (let m = 1; m <= 12; m++) {
      if (quarterOnly && ![3, 6, 9, 12].includes(m)) continue;
      const d = nthWeekday(y, m, 5, 2);
      if (d) out.push(d);
    }
  }
  return out;
}

