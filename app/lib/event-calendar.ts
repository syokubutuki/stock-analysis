// ---------------------------------------------------------------
// イベント StateFn の構築
// ---------------------------------------------------------------
// 反応日 index を求める。crossTiming=true なら「イベント日より後」、false なら「以降」で最初の営業日。
function reactionIndex(times: string[], eventDate: string, crossTiming: boolean, fromIdx: number): number {
  for (let i = fromIdx; i < times.length; i++) {
    if (crossTiming ? times[i] > eventDate : times[i] >= eventDate) return i;
  }
  return -1;
}

export interface EventStateOptions {
  market: Region; // この銘柄の市場（既定 JP）
  selected: string[]; // 表示するイベントID（優先順位はこの順）
}

// 各営業日を「どのイベントの反応日か」でラベル付けする StateFn を作る。
// 同じ営業日に複数イベントが重なる場合は selected の順で先勝ち。
export function buildEventStateFn(
  prices: PricePoint[],
  opts: EventStateOptions,
): StateFn {
  const times = prices.map((p) => p.time);
  const label = new Array<string | null>(prices.length).fill(null);
  const catalog = eventCatalog();
  const byId = new Map(catalog.map((e) => [e.id, e]));

  // selected の逆順で書き込む → 先頭が最終的に勝つ（先勝ち）
  for (let s = opts.selected.length - 1; s >= 0; s--) {
    const ev = byId.get(opts.selected[s]);
    if (!ev) continue;
    const cross = ev.region !== opts.market;
    const sortedDates = ev.dates.slice().sort();
    let ptr = 0;
    for (const d of sortedDates) {
      const idx = reactionIndex(times, d, cross, ptr);
      if (idx < 0) break;
      // 最終営業日は horizon が取れないが、集計側で除外されるのでラベルは付けてよい
      label[idx] = ev.label;
      ptr = idx; // 単調前進（次のイベントは今回以降を探す）
    }
  }

  const order = opts.selected.map((id) => byId.get(id)?.label ?? id).filter((l, i, a) => a.indexOf(l) === i);
  return {
    order,
    stateOf: (i: number) => label[i],
  };
}
