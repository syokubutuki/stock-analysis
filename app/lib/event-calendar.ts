// ④ イベントカレンダー条件付け：曜日ではなくマクロ発表日で先行きリターンを層別する
// -------------------------------------------------------------------------------
// 曜日効果は本質的に「イベント日程のノイズの多い代理変数」にすぎない。FOMC・CPI・雇用統計・
// 日銀・SQ といった実際のイベント日で条件付けるほうが、代理より本体を直接見ることになる。
//
// conditional-forward-returns.ts の StateFn/conditionalForwardReturns をそのまま使う。
// ここでは「各営業日がどのイベントの反応日か」を判定する StateFn を組み立てるだけ。
//
// ■ 反応日の定義（時差の扱い）
// 米国イベント（FOMC/CPI/雇用統計）は日本時間の夜に出るため、日本株が反応するのは
// イベント日の【翌営業日】。米国株なら【当日】（寄り前発表や当日午後の結果を当日終値が織り込む）。
// そこで event.region と market の一致/不一致で反応日を切り替える:
//   同一市場: 反応日 = イベント日以降で最初の営業日（当日反応）
//   異市場  : 反応日 = イベント日より後で最初の営業日（翌営業日反応, オーバーナイト波及）
//
// ■ 日付データの出所（重要）
//   - 雇用統計(NFP) と SQ は暦規則で厳密生成: NFP=毎月第1金曜、メジャーSQ=3/6/9/12月の
//     第2金曜、通常SQ=毎月第2金曜。これらは正確。
//   - FOMC/CPI/日銀 は暦規則が無いため静的な日付表をハードコード。公式スケジュールに基づく
//     best-effort であり、抜け・ずれの可能性がある（要検証・更新）。反応日は「イベント日以降/
//     以後の最初の営業日」に丸めるため、休日ずれや±1日程度の誤差はノイズとして減衰し、
//     偽のエッジは作らない（検出力が落ちるだけ）。

