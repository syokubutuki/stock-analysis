/** 導出鎖の1ステップ。数式は KaTeX 未導入のためプレーンテキスト（既存 AnalysisGuide に準拠）。 */
export interface DerivationStep {
  /** このステップで何をするか（例: 「損益に P の仮定を代入」）。 */
  label: string;
  /** 数式または論理（プレーンテキスト）。 */
  expr: string;
  /** 補足（なぜこの変形が許されるか、どの公準に依るか）。 */
  note?: string;
}

export interface Corollary {
  /** "C1" など。 */
  id: string;
  /** 既存理論名。 */
  theory: string;
  /** 一行要約（この系が何を主張するか）。 */
  claim: string;
  /** 立脚する公準・命題。 */
  basis: AxiomRef[];
  /** この系が最適化する q の自由。 */
  qChoice: QChoice[];
  /** 測る P の性質（この分析が記述する外生量）。 */
  pProperty: string;
  /** P に置く仮定（導出の前提）。 */
  assumption: string;
  /** 会計恒等式 W=∫q dP−C からの導出鎖。 */
  derivation: DerivationStep[];
  /** 得られる q* の閉形式（＝既存理論の公式そのもの）。 */
  formula: string;
  /** その公式が何を意味するか（結論の解釈）。 */
  conclusion: string;
  /** 公準5（摩擦）を入れると公式がどう変わるか。 */
  frictionEffect: string;
  /** 実装コンポーネント（app/components/analysis/ 配下 or /portfolio）。 */
  components: string[];
  /** 優先度（1..3, 3が最優先）。C3以降の追記順の指針。 */
  priority: 1 | 2 | 3;
}

